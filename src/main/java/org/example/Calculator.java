package org.example;

public class Calculator {

    public int calc(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public double div(int a, int b){
        return (double) a /b;
    }

    public double multi(int a, int b){
        return (double) a * b;
    }
}
